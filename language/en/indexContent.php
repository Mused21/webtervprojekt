      <h2>Ceremony</h2>
      <p>
        The prizes are mostly presented by Nobel laureates, originally at a ceremony in a lecture hall at <abbr title="Massachusetts Institute of Technology">MIT</abbr> but since 1994 in the
        Sanders Theater at Harvard University.Due to the COVID-19 pandemic, 2020 and 2021's event was held fully online. The event contains a number of running jokes, including Miss Sweetie Poo, a
        little girl who repeatedly cries out, <q>Please stop: I'm bored</q>, in a high-pitched voice if speakers go on too long.
        The awards ceremony is traditionally closed with the words:
      </p>
      <blockquote>"If you didn't win a prize—and especially if you did—better luck next year!"</blockquote>
