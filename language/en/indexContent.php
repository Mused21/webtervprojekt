      <h1>Welcome!</h1>
      <img id="logo" src="media/ignobellogo.png" alt="Ig Nobel logo" />
      <p>
        The Ig Nobels were created in 1991 by Marc Abrahams, editor and co-founder of the Annals of Improbable Research, a former editor-in-chief of the Journal of Irreproducible
        Results who has been master of ceremonies at all awards ceremonies. <dfn>Awards</dfn> were presented at that time for discoveries "that cannot, or <em>should not</em>, be reproduced".
        Ten prizes are awarded each year in many categories, including the Nobel Prize categories of physics, chemistry, physiology/medicine, literature, and peace, but also other categories
        such as public health, engineering, biology, and interdisciplinary research. The Ig Nobel Prizes recognize genuine achievements, with the exception of three prizes awarded in the first
        year to fictitious scientists Josiah S. Carberry, Paul DeFanti, and Thomas Kyle.
      </p>
      <p>
        The awards are sometimes criticism via satire, as in the two awards given for homeopathy research, prizes in "science education" to the Kansas State Department of Education and Colorado
        State Board of Education for their stance regarding the teaching of evolution, and the prize awarded to Social Text after the Sokal affair. Most often, however, they draw attention to
        scientific articles that have some humorous or unexpected aspect. Examples range from the discovery that the presence of humans tends to sexually arouse ostriches, to the statement that
        black holes fulfill all the technical requirements for being the location of Hell, to research on the "five-second rule", a tongue-in-cheek belief that food dropped on the floor will not
        become contaminated if it is picked up within five seconds.
      </p>
      <p>
        Sir Andre Geim, who had been awarded an Ig Nobel Prize in 2000 for levitating a frog by magnetism, was awarded a Nobel Prize in physics in 2010 for his work with the electromagnetic
        properties of graphene. He is the only individual, as of 2021, to have received both a Nobel and an Ig Nobel.
      </p>
