      <p>
        Los Ig Nobel fueron creados en 1991 por Marc Abrahams, editor y cofundador de Annals of Improbable Research, ex editor en jefe del Journal of Irreproducible
         Resultados quien ha sido maestro de ceremonias en todas las entregas de premios. <dfn>Premios</dfn> se entregaron en ese momento por descubrimientos "que no pueden, o <em>no deben</em>, ser reproducidos".
         Cada año se otorgan diez premios en muchas categorías, incluidas las categorías del Premio Nobel de física, química, fisiología/medicina, literatura y paz, pero también otras categorías.
         como salud pública, ingeniería, biología e investigación interdisciplinaria. Los premios Ig Nobel reconocen logros genuinos, con la excepción de tres premios otorgados en la primera
         año a los científicos ficticios Josiah S. Carberry, Paul DeFanti y Thomas Kyle.</p>
         <p>
           Los premios son a veces críticas a través de la sátira, como en los dos premios otorgados por investigación en homeopatía, premios en "educación científica" al Departamento de Educación del Estado de Kansas y Colorado.
           State Board of Education por su postura con respecto a la enseñanza de la evolución y el premio otorgado a Social Text después del asunto Sokal. La mayoría de las veces, sin embargo, llaman la atención sobre
           artículos científicos que tengan algún aspecto humorístico o inesperado. Los ejemplos van desde el descubrimiento de que la presencia de humanos tiende a excitar sexualmente a los avestruces hasta la afirmación de que
           los agujeros negros cumplen todos los requisitos técnicos para ser la ubicación del Infierno, para investigar sobre la "regla de los cinco segundos", una creencia irónica de que la comida que cae al suelo no
           contaminarse si se recoge en cinco segundos.
         </p>
         <p>
           Sir Andre Geim, que había sido galardonado con el Premio Ig Nobel en 2000 por levitar una rana por magnetismo, recibió el Premio Nobel de física en 2010 por su trabajo con el campo electromagnético.
           propiedades del grafeno. Es la única persona, a partir de 2021, que ha recibido un Nobel y un Ig Nobel.
         </p>
