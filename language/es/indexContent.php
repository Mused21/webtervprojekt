      <h2>Ceremonia</h2>
       <p>
         La mayoría de los premios los entregan los premios Nobel, originalmente en una ceremonia en una sala de conferencias en el <abbr title="Massachusetts Institute of Technology">MIT</abbr> pero desde 1994 en el
         Sanders Theatre en la Universidad de Harvard. Debido a la pandemia de COVID-19, el evento de 2020 y 2021 se llevó a cabo completamente en línea. El evento contiene una serie de chistes, incluyendo Miss Sweetie Poo, una
         niña pequeña que grita repetidamente, <q>Por favor, detente: estoy aburrida</q>, en un tono de voz agudo si los oradores se prolongan demasiado.
         La ceremonia de premiación se cierra tradicionalmente con las palabras:
       </p>
       <blockquote>"Si no ganaste un premio, y especialmente si lo hiciste, ¡mejor suerte el próximo año!"</blockquote>
